<?php declare(strict_types=1);
namespace System\Models;

enum PATTERN: string
{
    case INTEGER = '/^[0-9]*$/';
    case FLOAT = '/^[0-9\.]*$/';
    case ALPHABET = '/^[a-zA-Z]*$/';
    case ALPHANUM = '/^[a-zA-Z0-9\.]*$/';
    case DATE = '/^(19[0-9]{2}|2[0-9]{3}|[3-9]{4})-([1-9]|0[1-9]|1[0-2])-([1-9]|[12][0-9]|3[01])$/';
    case TIME = '/^([0-9]|0[0-9]|1[0-9]|2[0-4]):([0-9]|0[0-9]|[1-5][0-9]|60):([0-9]|0[0-9]|[1-5][0-9])$/';
    case DATETIME = '/^(19[0-9]{2}|2[0-9]{3}|[3-9]{4})-([1-9]|0[1-9]|1[0-2])-([1-9]|[12][0-9]|3[01]) ([0-9]|0[0-9]|1[0-9]|2[0-4]):([0-9]|0[0-9]|[1-5][0-9]|60):([0-9]|0[0-9]|[1-5][0-9])$/';
}